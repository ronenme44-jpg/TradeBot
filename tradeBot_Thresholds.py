def get_passed_level(pnl: float) -> int | None:
    """Return the highest ladder level reached by the current profit fraction."""
    # Rows are stored from low to high; keep overwriting until the best match.
    for level, profit_now, _tp, _sl in THRESHOLDS:
        if pnl >= profit_now:
            return level

    # If no row matched, we have not reached level 1 yet
    return None


def apply_level_no_downgrade(current_level: int | None, pnl: float) -> int | None:
    """Return the next higher level, or None when the trade should stay unchanged."""
    # Find the level that matches the current pnl (theoretical highest level)
    new_level = get_passed_level(pnl)

    # If we have not reached any level yet
    if new_level is None:
        return None

    # If we are not on any level yet (trade just started), enter the new level
    if current_level is None:
        return new_level

    # If the new level is higher than the current one, move up
    if new_level > current_level:
        return new_level

    # Otherwise, do not change level (we never downgrade)
    return None


def get_level_row(level: int) -> tuple[int, float, float, float]:
    """Return the full threshold row for the requested level."""
    for lvl, profit_now, tp_new, sl_new in THRESHOLDS:
        if lvl == level:
            return (lvl, profit_now, tp_new, sl_new)

    # If we get here, the requested level was not found in the table
    raise ValueError(f"Level {level} not found in THRESHOLDS")


def get_tp_sl_values(level: int) -> tuple[float, float]:
    """Return the take-profit and stop-loss fractions for one ladder level."""
    _lvl, _profit_now, tp_new, sl_new = get_level_row(level)
    return (tp_new, sl_new)
